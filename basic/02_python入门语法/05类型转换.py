#将数字类型转换成字符串
num_str = str(11)
print(type(num_str), num_str)

float_str = str(1.1)
print(type(float_str), float_str)

# 字符串转数字
num = int("11")
print(type(num), num)

num2 = float("12.13")
print(type(num2), num2)

